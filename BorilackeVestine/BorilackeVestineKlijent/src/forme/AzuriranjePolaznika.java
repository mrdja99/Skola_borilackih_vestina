    private void btnAzurirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAzurirajActionPerformed

         if(txtIme.getText().isEmpty() || txtPrezime.getText().isEmpty() || txtTelefon.getText().isEmpty() || txtEmail.getText().isEmpty() || txtGodina.getText().isEmpty() || txtDatum.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja su obavezna");
            return;
        }
        
        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        String telefon = txtTelefon.getText();
        int godina = Integer.parseInt(txtGodina.getText());
        
        if(godina > 30 || godina < 18) {
            JOptionPane.showMessageDialog(this, "Polaznik mora biti stariji od 18 , a mladji od 30 godina");
            return;
        }
        
        String email = txtEmail.getText();
        
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\."
                + "[a-zA-Z0-9_+&*-]+)*@"
                + "(?:[a-zA-Z0-9-]+\\.)+[a-z"
                + "A-Z]{2,7}$";
        
        Pattern pat = Pattern.compile(emailRegex);

        if (!pat.matcher(txtEmail.getText()).matches()) {
            JOptionPane.showMessageDialog(this, "Elektronska posta nije u dobrom formatu!");
            return;
        } 
        
        String datumUpis = txtDatum.getText();
        
        Date datum = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        
       
        try {
            datum = sdf.parse(datumUpis);
        } catch (ParseException ex) {
            Logger.getLogger(KreiranjePolaznika.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Datum mora biti u sledecem formatu: dd.MM.yyyy");
            return;
        }
        
        Polaznik p = new Polaznik(id, ime, prezime, telefon, godina, email, datum);
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.AZURIRAJ_POLAZNIKA);
        kz.setParametar(p);
        
        try {
            KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        } catch (InterruptedException ex) {
            Logger.getLogger(KreiranjePolaznika.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();
        
        if(so.isUspesno()){
            JOptionPane.showMessageDialog(this, so.getPoruka());
        }else{
            JOptionPane.showMessageDialog(this, so.getPoruka(), "Greska", JOptionPane.ERROR_MESSAGE);
        }

        PretrazivanjePolaznika pp = (PretrazivanjePolaznika) getParent();
        pp.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_btnAzurirajActionPerformed

