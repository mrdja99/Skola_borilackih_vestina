    void postavivrednosti(Polaznik p) {
        id = p.getPolaznikId();
        txtIme.setText(p.getIme());
        txtPrezime.setText(p.getPrezime());
        txtTelefon.setText(p.getTelefon());
        txtGodina.setText(String.valueOf(p.getGodina()));
        txtEmail.setText(p.getEmail());
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        txtDatum.setText(sdf.format(p.getDatumUpisa()));
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_KURSEVE_POLAZNIKA);
        kz.setParametar(p);
        
        
        try {
            KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        } catch (InterruptedException ex) {
            Logger.getLogger(AzuriranjePolaznika.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();

       
        ArrayList<Kurs> kurseviPolaznika = (ArrayList<Kurs>) so.getOdgovor();
        System.out.println(kurseviPolaznika);
        cmbKursPolaznika.removeAllItems();
        
        for (Kurs kurs : kurseviPolaznika) {
            cmbKursPolaznika.addItem(kurs);
        }
            
         

    }
