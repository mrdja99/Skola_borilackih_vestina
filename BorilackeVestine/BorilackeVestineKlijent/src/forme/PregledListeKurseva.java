    public void popuniTabelu() {
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_LISTU_KURSEVA);
        
        try {
            KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        } catch (InterruptedException ex) {
            Logger.getLogger(PregledListeKurseva.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();
        
        ArrayList<Kurs> kurseviBaza = (ArrayList<Kurs>) so.getOdgovor();
        
        ModelTabelaKurs mtk = (ModelTabelaKurs) tblKurs.getModel();
        mtk.popuniTabeluKurseva(kurseviBaza);
        
    }
