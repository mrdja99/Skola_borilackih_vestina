    private int vratiPolaznikID(Polaznik p) {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_IZABRANOG_POLAZNIKA);
        kz.setParametar(p);
        
        try {
            KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        } catch (InterruptedException ex) {
            Logger.getLogger(PretrazivanjePolaznika.class.getName()).log(Level.SEVERE, null, ex);
        }

        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();

        if (so.isUspesno()) {
            JOptionPane.showMessageDialog(this, so.getPoruka());
            return (int) so.getOdgovor();
        } else {
            JOptionPane.showMessageDialog(this, so.getPoruka(), "Greska", JOptionPane.ERROR_MESSAGE);
            return 0;
        }
        
    }

    private boolean postojiZakazivanje(Polaznik p, Trener trener) {
        ZakazivanjeTreninga zt = new ZakazivanjeTreninga(0, p, trener, null);
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.POSTOJI_ZAKAZIVANJE);
        kz.setParametar(zt);
        
        try {
            KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        } catch (InterruptedException ex) {
            Logger.getLogger(PretrazivanjePolaznika.class.getName()).log(Level.SEVERE, null, ex);
        }

        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();

        if (so.isUspesno()) {
            return (boolean) so.getOdgovor();
        } else {
            JOptionPane.showMessageDialog(this, so.getPoruka());
            return false;
        }
    }

    
