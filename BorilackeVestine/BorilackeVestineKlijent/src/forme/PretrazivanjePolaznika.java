    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        String filter = txtPrezime.getText().trim();
        
        Polaznik p = new Polaznik();
        
        if(filter.isEmpty()) {
            filter = "";
        }
        
        p.setFilter(filter);
        
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacije.VRATI_LISTU_POLAZNIKA);
        kz.setParametar(p);
        
        try {
            KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        } catch (InterruptedException ex) {
            Logger.getLogger(PretrazivanjePolaznika.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().primiOdgovor();
        
        if(so.isUspesno()) {
            ArrayList<Polaznik> polaznici = (ArrayList<Polaznik>) so.getOdgovor();
            ModelTabelaPolaznik mtp = (ModelTabelaPolaznik) tblPolaznik.getModel();
            mtp.popuniTabelu(polaznici);
            JOptionPane.showMessageDialog(this, so.getPoruka());
        }else {
            JOptionPane.showMessageDialog(this, so.getPoruka(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        int selectedRow = tblPolaznik.getSelectedRow();
        
        if(selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Molimo vas izaberite željenog polaznika");
            return;
        }
        
        ModelTabelaPolaznik mtp = (ModelTabelaPolaznik) tblPolaznik.getModel();
        Polaznik p = mtp.vratiPolaznika(selectedRow);
        p.setPolaznikId(vratiPolaznikID(p));
        
        if(p.getPolaznikId() != 0) {
            AzuriranjePolaznika ap = new AzuriranjePolaznika(this, true);
            ap.postavivrednosti(p);
            ap.setVisible(true);
        }
        
        
        
    }//GEN-LAST:event_btnDetaljiActionPerformed

    private void btnZakaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZakaziActionPerformed
        int selectedRow = tblPolaznik.getSelectedRow();
        
        if(selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Molimo vas izaberite željenog polaznika");
            return;
        }
        
        ModelTabelaPolaznik mtp = (ModelTabelaPolaznik) tblPolaznik.getModel();
        Polaznik p = mtp.vratiPolaznika(selectedRow);
        p.setPolaznikId(vratiPolaznikID(p));
        
        if(postojiZakazivanje(p, Kontroler.getInstance().getTrener())) {
            JOptionPane.showMessageDialog(this, "Vec je zakazan trening za ovog polaznika");
            PregledTreninga pt = new PregledTreninga();
            pt.setVisible(true);
        }else {
            ZakazivanjeTreningaForma zt = new ZakazivanjeTreningaForma();
            zt.setPolaznika(p);
            zt.setVisible(true);
        }
        
        
        
    }//GEN-LAST:event_btnZakaziActionPerformed

